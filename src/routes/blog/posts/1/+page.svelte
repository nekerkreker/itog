<p>
	AMD официально представила видеокарту Radeon RX 7600 за 269 долларов — на 29% быстрее RX 6600

Сегодня AMD официально объявила о видеокарте Radeon RX 7600 (не XT) и планирует выпустить ее уже завтра, всего через день после выхода видеокарты NVIDIA RTX 4060 Ti.
Графический процессор ускорителя обладает 2048 ядрами и разгоняется до 2,66 ГГц. Видеокарта оснащена 8 ГБ видеопамяти стандарта GDDR6 с шиной 128 бит и пропускной способностью 18 Гбит/с. Новинка поддерживает AV1 и имеет такой же объем Infinity Cache, как и Radeon RX 6600 – 32 МБ. Кроме того, AMD подтверждает, что аппаратная поддержка интерфейса DisplayPort 2.1 присутствует у карты RX 7600, но ее реализацией занимаются производители устройств.
Компания утверждает, что RX 7600 показывает производительность на 29% выше, чем RX 6600 при использовании разрешения 1080p и максимальных настройках графики.

Стоимость новинки составит 269 долларов, что значительно ниже цены RX 6600 при ее выходе, которая составляла 329 долларов.

</p>
